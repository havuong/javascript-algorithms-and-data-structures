// Iterate with JavaScript While Loops
var myArray = [];

var i = 0;
while (i < 5) {
    myArray.push(i);
    i++;
}

// Iterate with JavaScript For Loops
var myArray = [];

for (var i = 1; i <= 5; i++) {
    myArray.push(i);
}

// Iterate Odd Numbers With a For Loop
var myArray = [];

for (var i = 1; i < 10; i += 2) {
    myArray.push(i);
}